package com.example.be_e;

public class EVListInfo {
    String name, loce;
    Double lati, longti;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLoce() {
        return loce;
    }

    public void setLoce(String loce) {
        this.loce = loce;
    }

    public Double getLati() {
        return lati;
    }

    public void setLati(Double lati) {
        this.lati = lati;
    }

    public Double getLongti() {
        return longti;
    }

    public void setLongti(Double longti) {
        this.longti = longti;
    }
}
